public class Arithmetic {

    public static void main(String[] args) {
        
        //Declare variables
        int a = 1, b = 2;

        //Addition operator
        System.out.println("a + b = " + (a + b));

        //subtraction operator
        System.out.println("a - b = " + (a - b));

        //multiplication operator
        System.out.println("a * b = " + (a * b));

        //division operator
        System.out.println("a / b = " + (a / b));

        //modulo operator
        System.out.println("a % b = " + (a % b));

    }
}