public class AccessArrayElements {
    
    public static void main(String[] args) {
        
        int[] arr = {1, 5, -8, 6, 4};

        System.out.println("Print array element one by one:");
        System.out.println("1st element: " + arr[0]); //1
        System.out.println("2nd element: " + arr[1]); //5

    }
}
