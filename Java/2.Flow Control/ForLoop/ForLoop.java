class ForLoop {

    public static void main(String[] args) {
        
        int num = 10;

        //For loop
        for (int i = 0; i < num; i++) {
            System.out.println("Print karo 10 bar");            
        }

        for (int i = 1; i <= num; i++) {
            System.out.println(i);
        }
    }
}